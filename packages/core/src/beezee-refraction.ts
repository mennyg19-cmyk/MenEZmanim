/**
 * Get the BeeZee refraction value for a given Gregorian date.
 * This accounts for the Earth-Sun distance variation throughout the year.
 * Returns the refraction in degrees (typically 0.773-0.834).
 */
export function getDailyRefraction(month: number, day: number): number {
  const monthData = REFRACTION_TABLE[month];
  if (!monthData || day < 1 || day >= monthData.length) {
    return 0.8330; // fallback to a mid-range value
  }
  return monthData[day];
}
